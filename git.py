def stress_score(df, pct):
    out = df.copy()
    out["_z_news_s"] = z_s(df["news_7d_z_pm"].fillna(0)*(1+pct))
    out["_z_cp_s"]   = z_s(df["cp_score"].fillna(0)*(1+pct))
